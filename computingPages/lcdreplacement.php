<p>
  Step 1 in the project was a success. I did learn however that it is
  really very involved, so I planned to go very slow taking very
  detailed notes when I take my PowerBook apart. That one I definitely
  wanted to get back together again.
</p>

<h3>
  Step 2: Putting it in the powerbook.
</h3>

<p>
  First to kill the suspension, it worked (almost) perfect.
</p>

<center>
<a href="LCDimages/016-goodresult2-web.jpg">
<img src="LCDimages/016-goodresult2-small.jpg"
     alt="The Result" />
</a>
<br />
The Result
</center>

<p>
  The almost was because in the removing of the keyboard I broke a key
  (the R). I have already checked and a new (used) keyboard is $100,
  so a lot cheaper than an lcd. First I’d have to figure out how to
  properly remove keys, i.e., without breaking them.
</p>

<p>
  I just followed the instructions on ifixit. Interestingly enough I
  should not have. In the end I found that since I really just wanted
  to replace the lcd and not the frame holding it, I could just have
  opened the frame right away. If you look at the picture from
  yesterday (the one from the back), liberating the cable that you see
  there means taking the whole laptop apart, however, this cable can
  be removed. And if you do that all you have to do is take the bezel
  apart (for which ifixit does not have instructions, but is easy) and
  you can replace the lcd.
</p>

<p>
  Before giving the photos I took while doing this, some
  observations. In the instructions a screw is indicated as allowing
  you to remove some emi finger, however this screw actually only
  keeps the keyboard in place. Since I did not realize this I removed
  more keys from the keyboard (breaking the R) before figuring this
  out.
</p>

<p>
  The screws in the battery compartment are hard to reach. My screw
  driver was too long, so I had to work them under an angle (something
  my dad certainly would have shaken his finger at). Worked well
  enough in the end. Though getting them back in took a while.
</p>

<p>
  I am slightly worried about the heatsink. I have heard that when
  removing this, new heat conducting putty needs to be applied. Since
  I did not have this putty, I just tried it without. So far it seems
  to work well, the fan does appear to be more active than before
  though.
</p>

<p>
  The subwoofer was really strongly attached to the metal frame. In
  the end it only came of while I was working at opening the monitor
  frame.
</p>

<p>
  Opening the frame was really painful. I did not have instructions,
  and therefore I was not quite sure which of the two parts the dark
  grey plastic was supposed to go with. It worked by having it go with
  the back of the monitor. This was still very difficult to do though,
  and not having instructions I was only reassured it had worked when
  I had it back together again. Unfortunately this meant I forgot to
  take pictures.
</p>

<p>
  Lastly, the numbers of the two lcds are not exactly the same. The
  one from the iBook had number LP121X04 (C2), the one that came out
  of the PowerBook was LP 121X04 (C2) (K2). It has been on for a while
  now, and so this does not seem to matter.
</p>

