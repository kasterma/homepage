     <h1 align="center">
     Replacing an LCD in a Powerbook
     </h1>
  
<h3>
  Step 1: Removing an LCD from an iBook.
</h3>

<p>
  I have a 12″ PowerBook that had a broken lcd. A friend of mine had a
  12″ iBook with a good lcd but broken logic board. He gave me the
  remains of his laptop after removing the hard disk, memory, and one
  or two other parts that he will sell on ebay. Now I had the project
  of attempting to move the lcd from his iBook into my PowerBook.
</p>

<p>
  It was rather a big question if the lcd would fit. I had not been
  able to find reliable information about this on the internet (other
  than suggestions in the direction that it might), so I just gave it
  a shot.
</p>

<p>
  Since the friend removed some parts, his iBook was already partly
  disassembled. This resulted in the following starting setup.
  Starting setup.
</p>

<center>
<a href="LCDimages/002-overviewstart-web.jpg">
<img src="LCDimages/002-overviewstart-small.jpg"
     alt="The Starting Setup" />
</a>
<br />
Starting setup.
</center>

<p>
  The two broken laptops, and my then current working laptop to look
  up things in the internet if I get stuck. ifixit seemed to be a very
  good source of information about this. All tools I need to do this
  (spudger, #00 phillips screw driver, 4mm nut driver, and torx
  drivers of sizes 5 to 10) had arrived over the weekend from
  amazon.com and ifixit.com.
</p>

<p>
  The first order of business was to remove the lcd from the
  iBook. The strategy for this was to just remove many screws and
  remove parts until the lcd has been removed. From ifixit I had
  already learned that removing the lcd does require essentially to
  take the whole laptop apart.
</p>

<p>
  Since I did not intend to put this one back together I was not too
  careful about documenting the process, also twice I was sufficiently
  careless to actually break something: The bottom of the computer was
  extremely strongly attached where the cdrom drive was located. And
  the front of the monitor was fairly strongly attached to the back
  and made of rather soft material.
</p>

<p>
  This was the result.
</p>

